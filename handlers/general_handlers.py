async def button_back(message: types.Message, state: FSMContext):
    await state.clear()
    await message.reply("–í–µ—Ä–Ω—É–ª–∏—Å—å –≤ –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é.", reply_markup=get_main_keyboard())

async def echo_all(message: types.Message):
    await message.reply(
        "–Ø –ø–æ–Ω–∏–º–∞—é —Ç–æ–ª—å–∫–æ –∫–æ–º–∞–Ω–¥—ã –∏ –∫–Ω–æ–ø–∫–∏. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫–∏ –Ω–∏–∂–µ –∏–ª–∏ –≤–≤–µ–¥–∏—Ç–µ /help –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å–ø—Ä–∞–≤–∫–∏.",
        reply_markup=get_main_keyboard())

def register_general_handlers(dp):
    dp.message.register(send_welcome, CommandStart())
    dp.message.register(send_help, Command("help"))
    dp.message.register(button_back, lambda message: message.text == "üîô –í–µ—Ä–Ω—É—Ç—å—Å—è")
    dp.message.register(echo_all)
    
    return dp 